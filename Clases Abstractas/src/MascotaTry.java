public class MascotaTry {
    private String dueño;
    private String fechadevacuna;
    public MascotaTry(String dueño, String fechadevacuna) {
        this.dueño = dueño;
        this.fechadevacuna = fechadevacuna;
    }
    public String getDueño() {
        return dueño;
    }
    public void setDueño(String dueño) {
        this.dueño = dueño;
    }
    public String getFechadevacuna() {
        return fechadevacuna;
    }
    public void setFechadevacuna(String fechadevacuna) {
        this.fechadevacuna = fechadevacuna;
    }
    
}
